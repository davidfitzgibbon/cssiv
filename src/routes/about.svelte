<h1>What is CSS IV?</h1>

<p>CSS IV is a reaction to the debate that's been going on around CSS4 recently. The questions are around what would it do, what is it for, how would we maintain it etc etc.</p>

<p>Dave and Chris talked about all of this in <a href="https://shoptalkshow.com/399/">a recent episode of ShopTalkShow</a> and, I believe, nailed the arguement. The true question is "who is this for?!".</p>

<h2>Do users need CSS4?</h2>

<p>Ask anyone you know who isnt in the industry if they need CSS4. They wont know what you're talking about. No.</p>

<h2>Do browsers need CSS4?</h2>

<p>No. When browsers released more slowly, they could follow the spec and aim to hit CSS 2.1 or CSS3. Now that they're releasing more rapidly, they can just follow the latest spec. Having CSS4 might change how they name or version some things, but like HTML/JavaScript, they dont need specific versions.</p>

<h2>Do developers need CSS4?</h2>

<p>Often as developers we mish wish upon a star that a particular feature has been released ( looking at you container queries ). This is important though, we only wish the FEATURE was released. We dont care what you bundle it under, we just care that it's shipped in enough browsers that a reasonable percentage of our users will have it.</p>

<h2>Do educators need CSS4?</h2>

<p>Educators need to entice people to do their course ( whether a full on institution, or just someone selling a course online ). To this end they want to reassure you, the potential student, that what you'll be learning is relevant. Ok, we have something here. Versioning might be important.</p>
<p>CSS3 was a great marketing tool. You could ask someone "hey buddy, do you know CSS3", and they could tell you if they did or didnt. It also helped launch thousands of courses, tutorials and tips about CSS3. However, CSS3 was based on the format of the spec. The spec wasnt based on the wish of people marketing educational courses, it just hleped them.</p>

<h2>Conclusion</h2>

<p>We shouldnt have CSS4. The nature of spec creation has changed ( for the better ), so we shouldnt mess with that. However, that doesnt mean, for marketing purposes, we cant INFER a versioning for CSS.</p>
<p>And so, we can take the info from <a href="caniuse.com">caniuse.com/</a> mixed with the release date of browser versions to fudge a versioning of CSS by year.</p>

<h2>What's the future of this?</h2>

<p>At the moment this is just a reaction, but if it gains traction and is useful to people there's no reason it cant be improved, refined and made more accurate!</p>

